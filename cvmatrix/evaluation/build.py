from cvmatrix.utils.registry import Registry


EVALUATION_REGISTRY = Registry("EVALUATION")
EVALUATION_REGISTRY.__doc__ = """
build registry for evalution.
"""


def build_evaluator(cfg):
    assert 'type' in cfg.keys(), f'key "type" not be implemented in config'
    eval_cfg = dict(cfg)
    eval_name = eval_cfg.pop('type')
    evaluator = EVALUATION_REGISTRY.get(eval_name)(**eval_cfg)
    return evaluator


